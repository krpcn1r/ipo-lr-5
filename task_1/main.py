text = input("Введите строку: ") # Запрашиваем строку
glas = ["а","о","у","э","ы","и","я","е","ё","ю"] # Список гласных
soglas = ["б","в","г","д","ж","з","й","к","л","м","н","п","р","с","т","ф","х","ц","ч","ш","щ"] # Список согласных
count_a = 0
count_b = 0

for i in text.lower():
    if i in glas:
        count_a += 1
    elif i in soglas:
        count_b += 1
print(f"Согласных - {count_b}, гласных - {count_a}")
    
